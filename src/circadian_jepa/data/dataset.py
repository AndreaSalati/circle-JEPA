import anndata
import numpy as np
import torch
from torch.utils.data import Dataset

from .views import ViewGenerator


class CircadianDataset(Dataset):
    """PyTorch Dataset wrapping an AnnData object.

    Each item is a pair of augmented views of one cell, generated by the
    ViewGenerator. true_phase and batch_label are included when available
    (evaluation only — never used in the JEPA loss).
    """

    def __init__(
        self,
        adata: anndata.AnnData,
        view_generator: ViewGenerator,
        use_raw_counts: bool = True,
    ) -> None:
        """
        Parameters
        ----------
        adata:
            Preprocessed AnnData. Raw counts are taken from .layers['counts']
            when use_raw_counts=True and the layer is present; otherwise .X is used.
        view_generator:
            Controls how views are generated (downsampling / same-batch pairing).
        use_raw_counts:
            Whether to use .layers['counts'] for view generation.
        """
        self.vg = view_generator

        if use_raw_counts and "counts" in adata.layers:
            x = adata.layers["counts"]
        else:
            x = adata.X
        x = x.toarray() if hasattr(x, "toarray") else np.asarray(x)
        self._counts = torch.tensor(x, dtype=torch.float32)

        self._true_phase: torch.Tensor | None = (
            torch.tensor(adata.obs["true_phase"].values, dtype=torch.float32)
            if "true_phase" in adata.obs
            else None
        )

        batch_key = view_generator.batch_key
        self._batch_labels_str: np.ndarray | None = (
            adata.obs[batch_key].values if batch_key in adata.obs else None
        )

        # Pre-compute same-batch partner indices once
        self._partner_idx: torch.Tensor | None = None
        if view_generator.same_batch and self._batch_labels_str is not None:
            codes = adata.obs[batch_key].astype("category").cat.codes.values
            batch_int = torch.tensor(codes, dtype=torch.long)
            self._partner_idx = view_generator._sample_partners(batch_int)

    def __len__(self) -> int:
        return self._counts.shape[0]

    def __getitem__(self, idx: int) -> dict:
        counts = self._counts[idx]  # (n_genes,)

        if self.vg.same_batch and self._partner_idx is not None:
            partner = self._counts[self._partner_idx[idx]]
            view_a, _ = self.vg.make_pair(counts.unsqueeze(0))
            view_b, _ = self.vg.make_pair(partner.unsqueeze(0))
            view_a = view_a.squeeze(0)
            view_b = view_b.squeeze(0)
        else:
            view_a, view_b = self.vg.make_pair(counts.unsqueeze(0))
            view_a = view_a.squeeze(0)
            view_b = view_b.squeeze(0)

        out: dict = {"view_a": view_a, "view_b": view_b}
        if self._batch_labels_str is not None:
            out["batch_label"] = self._batch_labels_str[idx]
        if self._true_phase is not None:
            out["true_phase"] = self._true_phase[idx]
        return out
